print('Mengjitung luas dan keliling persegi panjang')

P = float(input('Masukkan panjang persegi panjang: ')) #input panjang dalam bentuk float
L = float(input('Masukkan lebar persegi panjang: '))
#input panjang dan lebar dalam bentuk float

#rumus mengitung luas persegi panjang (Panjang x Lebar)
Luas = P * L

#Rumus menghitung keliling persegi panjang (2 x (Panjang + Lebar))
Keliling = 2 * (P + L)

print(f"Luas perssegi panjang: {Luas}")     #Tampil Luas
print(f"Keliling persegi panjang: {Keliling}")      #Tampil Keliling