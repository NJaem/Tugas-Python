a = 99          # <class 'int'>
print(a)

b = 3.10        # <class 'float'>
print(b)

c = 'Python'    # <class 'str'>
print(c)

d = True        # <class 'bool'>
print(d)

e = [2, 4, 9]   # <class 'list'>
print(e)

f = (2, 5, 8)   # <class 'tuple'>
print(f)

g = {20, 35, 99}   # <class 'set'>
print(g)

h = {'Nama': 'Dedes', 'umur': 21} # <class 'dict'>
print(h)

print(type(a), type(b), type(c), type(d), type(e), type(f), type(g), type(h))